public class prg31 {
    static int sq(int a){
        return a*a;
    }
    static int cube(int b){
        int ans=b*b*b;
        return ans;
    }
    public static void main(String[] args) {
        System.out.println("program starts");
        System.out.println(sq(2));
        System.out.println(cube(3));
        System.out.println("program ends");
    }
    
}
